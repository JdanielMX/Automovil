    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed


          if (tblAutos.getRowCount() > 0) {

            if (tblAutos.getSelectedRow() != -1) {

                int indice = Integer.parseInt(String.valueOf(tblAutos.getValueAt(tblAutos.getSelectedRow(), 0)));

                ActualizarDatos actualizaDatos = new ActualizarDatos(indice);
                actualizaDatos.setVisible(true);
                actualizaDatos.setLocationRelativeTo(null);
                this.dispose();

            } else {

                JOptionPane.showMessageDialog(null, "No has seleccionado el valor a actualizar");

            }

        } else {

            JOptionPane.showMessageDialog(null, "No hay Datos en la tabla");

        }



    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed


        
        
        if (tblAutos.getRowCount() > 0) {

            if (tblAutos.getSelectedRow() != -1) {

                int opcion = JOptionPane.showConfirmDialog(null, "Â¿Estas seguro de eliminar el elemento seleccionado?");

                if (opcion == 0) {

                    int indice = Integer.parseInt(String.valueOf(tblAutos.getValueAt(tblAutos.getSelectedRow(), 0)));
                    controlador.eliminarDatos(indice);
                    JOptionPane.showMessageDialog(null, "Datos eliminados");
                    cargarTabla();
                }

            } else {

                JOptionPane.showMessageDialog(null, "No has seleccionado el valor a eliminar");

            }

        } else {

            JOptionPane.showMessageDialog(null, "No hay Datos en la tabla");

        }


    }//GEN-LAST:event_btnEliminarActionPerformed

  private void cargarTabla() {

        DefaultTableModel tabla = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int r, int c) {
                return false;
            }
        };

       
        String[] titulos = {"ID", "MARCA", "MODELO", "MOTOR", "PLACAS", "CANTIDADPUERTAS"};

        tabla.setColumnIdentifiers(titulos);

        List<Automovil> lista = new ArrayList();

          lista = controlador.mostrarAutos();

        if (!lista.isEmpty()) {

            for (Automovil auto : lista) {

                Object[] datos = {auto.getId(), auto.getMarca(), auto.getModelo(), auto.getMotor(),
                    auto.getPlacas(), auto.getCantidadPuertas()};

                tabla.addRow(datos);

            }

        }

        tblAutos.setModel(tabla);

    }


